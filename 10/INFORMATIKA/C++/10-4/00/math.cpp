#include <bits/stdc++.h>
using namespace std;
int main()
{
	int sari_roti = 5000;
	int aqua = 3000;
	int mie_samyang = 25000;
	int telur_1kg = 27000;
	
	int total = sari_roti + aqua + mie_samyang + telur_1kg;
	cout << "Total belanja = " << total << endl;
	
	cout << "100 - 60 = " << 100 - 60 << endl;
	cout << "20 x 5 = " << 20 * 5 << endl;
	cout << "40 : 10 = " << 40 / 10 << endl;
	cout << 100 + 3 * 6 - 10 << endl;
	cout << 10 * (1 + 4) - 3 << endl;
	cout << "10 % 3 = " << 10 % 3 <<  endl;
	
	int saldo = 3000000;
	// saldo = saldo - 500000;
	saldo -= 500000;
	cout << saldo << endl;
	
	int b = 10;
	b += 7; 
	cout << b << endl; // 17

	int c = 100;
	c -= 10;
	cout << c << endl; // 90
	
	int d = 50;
	d *= 2;
	cout << d << endl; // 100
	
	int e = 100;
	e /= 50;
	cout <<	e << endl; // 2
	
	f = 10;
	f++; // f = f + 1
	cout << f << endl;
	
	g = 20;
	++g;
	cout << g << endl;
	
	h = 100;
	h--;
	cout << h << endl;
	
	a = 10;
	cout << 10 + ++a * 3 - 5 << endl;
	
}

