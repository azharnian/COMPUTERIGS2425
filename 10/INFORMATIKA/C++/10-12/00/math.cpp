#include <iostream>
using namespace std;
int main()
{
	cout << "12 + 2 = " << 12 + 2 << endl;
	cout << "20 - 5 = " << 20 - 5 << endl;
	cout << "20 x 5 = " << 20 * 5 << endl;
	cout << "100 : 20 = " << 100 / 20 << endl;
	cout << "10 % 3 = " << 10 % 3 << endl;
	
	cout << 10 + 2 * 5 - 5 << endl;
	cout << 5 * (2 + 1) - 3 << endl;
	cout << 20 % 3 - 5 * 2 + 7 << endl;
}
