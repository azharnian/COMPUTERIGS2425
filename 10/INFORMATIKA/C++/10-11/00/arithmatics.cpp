#include <iostream>
using namespace std;

int main()
{
	cout << "12 + 5 = " << 12 + 5 << endl;
	cout << "100 - 50 = " << 100 - 50 << endl;
	cout << "20 x 5 = " << 20 * 5 << endl;
	cout << "100 : 20 = " << 100 / 20 << endl;
	cout << "10 % 3 = " << 10 % 3 << endl;
	
	cout << 100 + 50 * 2 - 30 << endl;
	cout << (2 + 10) * 4 - 7 << endl;
	cout << 10 + 20 % 3 - 2 << endl;
	 
}

